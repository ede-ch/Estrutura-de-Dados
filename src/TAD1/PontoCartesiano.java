package TAD1;

public class PontoCartesiano {

    // atributos
    private double x; 
    private double y;

    // construtor
    public PontoCartesiano(double x, double y) {
        this.x = x;
        this.y = y;
    }

    // m√©todos getters e setters
    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }
}












    
